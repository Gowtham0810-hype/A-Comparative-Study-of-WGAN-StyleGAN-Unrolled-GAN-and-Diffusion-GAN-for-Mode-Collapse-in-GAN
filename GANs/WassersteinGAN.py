# 1. 2D Synthetic Metrics (Wasserstein & Mode Capture)
# ---------------------------------------------------------
def eval_2d_grid_modes(generator, num_samples=10000, threshold=0.1):
    """Calculates how many of the 25 grid modes the generator captures."""
    generator.eval()
    with torch.no_grad():
        z = torch.randn(num_samples, Z_DIM).to(DEVICE)
        fake_points = generator(z).cpu().numpy()

    # Define the 25 actual mode centers (-4, -2, 0, 2, 4 for both X and Y)
    centers = []
    for i in range(-2, 3):
        for j in range(-2, 3):
            centers.append([i*2, j*2])
    centers = np.array(centers)

    captured_modes = set()
    for point in fake_points:
        # Find the Euclidean distance to all centers
        distances = np.linalg.norm(centers - point, axis=1)
        nearest_center_idx = np.argmin(distances)

        # If the generated point is close enough to a center, count it as captured
        if distances[nearest_center_idx] < threshold:
            captured_modes.add(nearest_center_idx)

    print(f"[2D Grid] Modes Captured: {len(captured_modes)} / 25")
    return len(captured_modes)

def calc_1d_wasserstein(real_dataloader, generator, num_samples=1000):
    """Approximates Wasserstein distance using flattened 1D distributions."""
    generator.eval()
    real_batch = next(iter(real_dataloader))[0].view(real_dataloader.batch_size, -1).cpu().numpy()

    with torch.no_grad():
        z = torch.randn(real_batch.shape[0], Z_DIM).to(DEVICE)
        fake_batch = generator(z).view(real_batch.shape[0], -1).cpu().numpy()

    # Calculate across the flattened batch
    w_dist = wasserstein_distance(real_batch.flatten(), fake_batch.flatten())
    print(f"Empirical Wasserstein Distance: {w_dist:.4f}")
    return w_dist

