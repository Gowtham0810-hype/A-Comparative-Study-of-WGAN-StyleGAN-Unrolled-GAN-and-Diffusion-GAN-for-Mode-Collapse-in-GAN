class GeneratorMLP(nn.Module):
    def __init__(self, z_dim=Z_DIM, out_dim=2):
        super().__init__()
        self.model = nn.Sequential(
            nn.Linear(z_dim, 128), nn.ReLU(True),
            nn.Linear(128, 256), nn.ReLU(True),
            nn.Linear(256, 256), nn.ReLU(True),
            nn.Linear(256, out_dim)
        )
    def forward(self, z): return self.model(z)

class CriticMLP(nn.Module):
    def __init__(self, in_dim=2):
        super().__init__()
        self.model = nn.Sequential(
            nn.Linear(in_dim, 256), nn.LeakyReLU(0.2, inplace=True),
            nn.Linear(256, 256), nn.LeakyReLU(0.2, inplace=True),
            nn.Linear(256, 128), nn.LeakyReLU(0.2, inplace=True),
            nn.Linear(128, 1)
        )
    def forward(self, x): return self.model(x)

def compute_gradient_penalty(critic, real_samples, fake_samples, device):
    """Calculates the gradient penalty loss for WGAN GP"""
    # Random weight term for interpolation between real and fake samples
    if real_samples.dim() == 4: # Image data
        alpha = torch.rand((real_samples.size(0), 1, 1, 1), device=device)
    else:                       # 2D data
        alpha = torch.rand((real_samples.size(0), 1), device=device)

    # Get random interpolation between real and fake samples
    interpolates = (alpha * real_samples + ((1 - alpha) * fake_samples)).requires_grad_(True)
    d_interpolates = critic(interpolates)

    fake = torch.ones((real_samples.shape[0], 1), device=device)

    # Get gradient w.r.t. interpolates
    gradients = autograd.grad(
        outputs=d_interpolates,
        inputs=interpolates,
        grad_outputs=fake,
        create_graph=True,
        retain_graph=True,
        only_inputs=True,
    )[0]

    gradients = gradients.view(gradients.size(0), -1)
    gradient_penalty = ((gradients.norm(2, dim=1) - 1) ** 2).mean()
    return gradient_penalty

def train_wgan_gp(dataset_name, dataloader, is_mlp=False, channels=3, epochs=100, n_critic=5, lambda_gp=10):
    print(f"--- Starting Training for {dataset_name} ---")

    # Initialize networks
    if is_mlp:
        generator = GeneratorMLP(Z_DIM).to(DEVICE)
        critic = CriticMLP().to(DEVICE)
    else:
        generator = GeneratorCNN(Z_DIM, channels, IMG_SIZE).to(DEVICE)
        critic = CriticCNN(channels, IMG_SIZE).to(DEVICE)

    # Optimizers (Adam is fine for WGAN-GP, standard WGAN uses RMSprop)
    optimizer_G = optim.Adam(generator.parameters(), lr=1e-4, betas=(0.0, 0.9))
    optimizer_C = optim.Adam(critic.parameters(), lr=1e-4, betas=(0.0, 0.9))

    for epoch in range(epochs):
        for i, batch in enumerate(dataloader):
            real_data = batch[0].to(DEVICE) if isinstance(batch, (list, tuple)) else batch.to(DEVICE)
            current_batch_size = real_data.size(0)

            # ---------------------
            #  Train Critic
            # ---------------------
            optimizer_C.zero_grad()

            # Sample noise
            z = torch.randn(current_batch_size, Z_DIM, device=DEVICE)

            # Generate a batch of images
            fake_data = generator(z)

            # Real and Fake validity
            real_validity = critic(real_data)
            fake_validity = critic(fake_data.detach())

            # Gradient penalty
            gradient_penalty = compute_gradient_penalty(critic, real_data.data, fake_data.data, DEVICE)

            # Adversarial loss
            c_loss = -torch.mean(real_validity) + torch.mean(fake_validity) + lambda_gp * gradient_penalty
            c_loss.backward()
            optimizer_C.step()

            # ---------------------
            #  Train Generator
            # ---------------------
            optimizer_G.zero_grad()

            # Train the generator every n_critic steps
            if i % n_critic == 0:
                # Generate a batch of images
                fake_images = generator(z)
                # Loss measures generator's ability to fool the critic
                g_loss = -torch.mean(critic(fake_images))

                g_loss.backward()
                optimizer_G.step()

        if epoch % 10 == 0:
            print(f"[Epoch {epoch}/{epochs}] [C loss: {c_loss.item():.4f}] [G loss: {g_loss.item():.4f}]")

    return generator

