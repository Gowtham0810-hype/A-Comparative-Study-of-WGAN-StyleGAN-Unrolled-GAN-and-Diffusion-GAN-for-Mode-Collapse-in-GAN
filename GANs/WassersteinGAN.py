class GeneratorCNN(nn.Module):
    def __init__(self, z_dim=Z_DIM, channels=3, img_size=IMG_SIZE):
        super().__init__()
        self.init_size = img_size // 16  # For 64x64, this is 4
        self.l1 = nn.Sequential(nn.Linear(z_dim, 512 * self.init_size ** 2))

        self.conv_blocks = nn.Sequential(
            nn.BatchNorm2d(512),
            nn.Upsample(scale_factor=2), # 8x8
            nn.Conv2d(512, 256, 3, stride=1, padding=1),
            nn.BatchNorm2d(256),
            nn.ReLU(True),
            nn.Upsample(scale_factor=2), # 16x16
            nn.Conv2d(256, 128, 3, stride=1, padding=1),
            nn.BatchNorm2d(128),
            nn.ReLU(True),
            nn.Upsample(scale_factor=2), # 32x32
            nn.Conv2d(128, 64, 3, stride=1, padding=1),
            nn.BatchNorm2d(64),
            nn.ReLU(True),
            nn.Upsample(scale_factor=2), # 64x64
            nn.Conv2d(64, channels, 3, stride=1, padding=1),
            nn.Tanh(),
        )

    def forward(self, z):
        out = self.l1(z)
        out = out.view(out.shape[0], 512, self.init_size, self.init_size)
        return self.conv_blocks(out)

class CriticCNN(nn.Module):
    def __init__(self, channels=3, img_size=IMG_SIZE):
        super().__init__()
        def critic_block(in_filters, out_filters, bn=True):
            block = [nn.Conv2d(in_filters, out_filters, 4, 2, 1), nn.LeakyReLU(0.2, inplace=True)]
            if bn:
                # WGAN-GP replaces standard BatchNorm with LayerNorm or avoids it in Critic
                block.append(nn.LayerNorm([out_filters, img_size//2, img_size//2]) if out_filters==64 else nn.Identity())
            return block

        self.model = nn.Sequential(
            *critic_block(channels, 64, bn=False),     # 32x32
            nn.Conv2d(64, 128, 4, 2, 1),               # 16x16
            nn.LeakyReLU(0.2, inplace=True),
            nn.Conv2d(128, 256, 4, 2, 1),              # 8x8
            nn.LeakyReLU(0.2, inplace=True),
            nn.Conv2d(256, 512, 4, 2, 1),              # 4x4
            nn.LeakyReLU(0.2, inplace=True),
        )
        ds_size = img_size // 16
        self.adv_layer = nn.Linear(512 * ds_size ** 2, 1)

    def forward(self, img):
        out = self.model(img)
        out = out.view(out.shape[0], -1)
        return self.adv_layer(out)

