# 1. Core StyleGAN Components
# ---------------------------------------------
class MappingNetwork(nn.Module):
    """Maps standard latent Z to style latent W"""
    def __init__(self, z_dim, w_dim, num_layers=4): # Kept to 4 for speed (official is 8)
        super().__init__()
        layers = []
        for _ in range(num_layers):
            layers.extend([nn.Linear(z_dim, w_dim), nn.LeakyReLU(0.2, inplace=True)])
            z_dim = w_dim
        self.net = nn.Sequential(*layers)

    def forward(self, z):
        return self.net(z)

class AdaIN(nn.Module):
    """Adaptive Instance Normalization to inject Style (W) into convolutions"""
    def __init__(self, channels, w_dim):
        super().__init__()
        self.instance_norm = nn.InstanceNorm2d(channels, affine=False)
        self.style_scale = nn.Linear(w_dim, channels)
        self.style_shift = nn.Linear(w_dim, channels)

    def forward(self, x, w):
        normalized = self.instance_norm(x)
        gamma = self.style_scale(w).unsqueeze(2).unsqueeze(3)
        beta = self.style_shift(w).unsqueeze(2).unsqueeze(3)
        return normalized * (gamma + 1.0) + beta

