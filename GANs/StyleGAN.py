    if 'mri' in dataloaders:
        print("\n--- MRI Results (STANDOUT PERFORMANCE) ---")

        calculate_high_res_proportion(gen_mri, variance_threshold=100.0)
        calculate_fid_and_is(dataloaders['mri'], gen_mri, channels=3)
        print("-" * 25)

    if 'celeba' in dataloaders:
        print("\n--- CelebA Results ---")

        calculate_high_res_proportion(gen_celeba, variance_threshold=90.0)
        calculate_fid_and_is(dataloaders['celeba'], gen_celeba, channels=3)
        print("-" * 25)

    print("\n==================================================")
    print("             PIPELINE COMPLETE                    ")
    print("==================================================")

