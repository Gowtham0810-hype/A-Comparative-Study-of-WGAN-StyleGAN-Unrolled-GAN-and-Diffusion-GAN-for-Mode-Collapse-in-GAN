    if 'celeba' in dataloaders:
        print("\nTraining CelebA Generator (StyleGAN-Lite)...")
        gen_celeba = train_stylegan("CelebA", dataloaders['celeba'], is_mlp=False, channels=3, epochs=TRAIN_EPOCHS)

    print("\n\n>>> PHASE 2: COMPUTATIONAL EVALUATIONS (PAPER CONCLUSIONS) <<<\n")

    print("--- 2D Grid Results ---")

    eval_2d_grid_modes(gen_grid)
    calc_1d_wasserstein(dataloaders['2d_grid'], gen_grid)
    print("-" * 25)

    print("\n--- MNIST Results ---")

    calculate_high_res_proportion(gen_mnist, variance_threshold=75.0)
    calculate_fid_and_is(dataloaders['mnist'], gen_mnist, channels=1)
