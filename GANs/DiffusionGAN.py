class GeneratorMLP(nn.Module):
    def __init__(self, z_dim=Z_DIM, out_dim=2):
        super().__init__()
        self.model = nn.Sequential(
            nn.Linear(z_dim, 128), nn.ReLU(True),
            nn.Linear(128, 256), nn.ReLU(True),
            nn.Linear(256, 256), nn.ReLU(True),
            nn.Linear(256, out_dim)
        )
    def forward(self, z): return self.model(z)

class DiscriminatorMLP(nn.Module):
    def __init__(self, in_dim=2):
        super().__init__()
        self.model = nn.Sequential(
            nn.Linear(in_dim, 256), nn.LeakyReLU(0.2, inplace=True),
            nn.Linear(256, 256), nn.LeakyReLU(0.2, inplace=True),
            nn.Linear(256, 128), nn.LeakyReLU(0.2, inplace=True),
            nn.Linear(128, 1)
        )
    def forward(self, x): return self.model(x)

def apply_diffusion_noise(data, t, max_t=1000):
    """
    Applies forward diffusion to the data (images or 2D coordinates)
    based on the sampled timestep t.
    """
    beta_start = 0.0001
    beta_end = 0.02
    betas = torch.linspace(beta_start, beta_end, max_t).to(data.device)
    alphas = 1. - betas
    alphas_cumprod = torch.cumprod(alphas, dim=0)

    # Reshape based on data dimensions (4D for images, 2D for points)
    if data.dim() == 4:
        sqrt_alphas_cumprod = torch.sqrt(alphas_cumprod[t]).view(-1, 1, 1, 1)
        sqrt_one_minus_alphas_cumprod = torch.sqrt(1. - alphas_cumprod[t]).view(-1, 1, 1, 1)
    else:
        sqrt_alphas_cumprod = torch.sqrt(alphas_cumprod[t]).view(-1, 1)
        sqrt_one_minus_alphas_cumprod = torch.sqrt(1. - alphas_cumprod[t]).view(-1, 1)

    noise = torch.randn_like(data)
    noisy_data = sqrt_alphas_cumprod * data + sqrt_one_minus_alphas_cumprod * noise
    return noisy_data

def train_diffusion_gan(dataset_name, dataloader, is_mlp=False, channels=3, epochs=50):
    print(f"--- Starting Diffusion GAN Training for {dataset_name} ---")

    if is_mlp:
        generator = GeneratorMLP(Z_DIM).to(DEVICE)
        discriminator = DiscriminatorMLP().to(DEVICE)
    else:
        generator = GeneratorCNN(Z_DIM, channels, IMG_SIZE).to(DEVICE)
        discriminator = DiscriminatorCNN(channels, IMG_SIZE).to(DEVICE)

    criterion = nn.BCEWithLogitsLoss()
    opt_G = optim.Adam(generator.parameters(), lr=0.0002, betas=(0.5, 0.999))
    opt_D = optim.Adam(discriminator.parameters(), lr=0.0002, betas=(0.5, 0.999))

    for epoch in range(epochs):
        for i, batch in enumerate(dataloader):
            real_data = batch[0].to(DEVICE) if isinstance(batch, (list, tuple)) else batch.to(DEVICE)
            batch_size = real_data.size(0)

            real_labels = torch.ones(batch_size, 1).to(DEVICE)
            fake_labels = torch.zeros(batch_size, 1).to(DEVICE)

            # Sample random diffusion timestep t for this batch
            # Note: In practice, t is annealed over training so less noise is added later.
            max_t = 1000
            current_max_t = max(1, max_t - int((epoch / epochs) * max_t))
            t = torch.randint(0, current_max_t, (batch_size,), device=DEVICE).long()

            # ==========================================
            # 1. Train Discriminator on Diffused Data
            # ==========================================
            opt_D.zero_grad()
            z = torch.randn(batch_size, Z_DIM).to(DEVICE)
            fake_data = generator(z)

            # Apply identical noise to Real and Fake
            diffused_real = apply_diffusion_noise(real_data, t, max_t)
            diffused_fake = apply_diffusion_noise(fake_data.detach(), t, max_t)

            d_real_loss = criterion(discriminator(diffused_real), real_labels)
            d_fake_loss = criterion(discriminator(diffused_fake), fake_labels)
            d_loss = d_real_loss + d_fake_loss
            d_loss.backward()
            opt_D.step()

            # ==========================================
            # 2. Train Generator through Diffused Discriminator
            # ==========================================
            opt_G.zero_grad()
            z_g = torch.randn(batch_size, Z_DIM).to(DEVICE)
            gen_data = generator(z_g)

            # Apply noise to generated data before evaluating
            diffused_gen = apply_diffusion_noise(gen_data, t, max_t)
            g_loss = criterion(discriminator(diffused_gen), real_labels)

            g_loss.backward()
            opt_G.step()

        if epoch % 10 == 0:
            print(f"[Epoch {epoch}/{epochs}] [D loss: {d_loss.item():.4f}] [G loss: {g_loss.item():.4f}]")

    return generator

import numpy as np
from scipy.stats import wasserstein_distance
from torchmetrics.image.fid import FrechetInceptionDistance
from torchmetrics.image.inception import InceptionScore

def eval_2d_grid_modes(generator, num_samples=10000, threshold=0.1):
    generator.eval()
    with torch.no_grad():
        z = torch.randn(num_samples, Z_DIM).to(DEVICE)
        fake_points = generator(z).cpu().numpy()

    centers = np.array([[i*2, j*2] for i in range(-2, 3) for j in range(-2, 3)])
    captured_modes = set()

    for point in fake_points:
        distances = np.linalg.norm(centers - point, axis=1)
        nearest_idx = np.argmin(distances)
        if distances[nearest_idx] < threshold:
            captured_modes.add(nearest_idx)

    print(f"[2D Grid] Modes Captured: {len(captured_modes)} / 25")
    return len(captured_modes)

def calc_1d_wasserstein(real_dataloader, generator):
    generator.eval()
    real_batch = next(iter(real_dataloader))[0].view(real_dataloader.batch_size, -1).cpu().numpy()
    with torch.no_grad():
        z = torch.randn(real_batch.shape[0], Z_DIM).to(DEVICE)
        fake_batch = generator(z).view(real_batch.shape[0], -1).cpu().numpy()

    w_dist = wasserstein_distance(real_batch.flatten(), fake_batch.flatten())
    print(f"Empirical Wasserstein Distance: {w_dist:.4f}")
    return w_dist

def calculate_fid_and_is(real_dataloader, generator, channels=3, num_batches=10):
    generator.eval()
    fid = FrechetInceptionDistance(feature=2048).to(DEVICE)
    inc_score = InceptionScore().to(DEVICE)

    with torch.no_grad():
        for i, batch in enumerate(real_dataloader):
            if i >= num_batches: break
            real_imgs = batch[0].to(DEVICE)
            if channels == 1: real_imgs = real_imgs.repeat(1, 3, 1, 1)
            real_imgs = ((real_imgs + 1) / 2 * 255).byte()
            fid.update(real_imgs, real=True)

            z = torch.randn(real_imgs.size(0), Z_DIM).to(DEVICE)
            fake_imgs = generator(z)
            if channels == 1: fake_imgs = fake_imgs.repeat(1, 3, 1, 1)
            fake_imgs = ((fake_imgs + 1) / 2 * 255).byte()
            fid.update(fake_imgs, real=False)
            inc_score.update(fake_imgs)

    fid_score = fid.compute().item()
    is_mean, is_std = inc_score.compute()
    print(f"FID Score: {fid_score:.2f}")
    print(f"Inception Score: {is_mean.item():.2f} ± {is_std.item():.2f}")

def calculate_high_res_proportion(generator, num_samples=1000, variance_threshold=100.0):
    import torch.nn.functional as F
    generator.eval()
    laplacian_kernel = torch.tensor([[[[0., 1., 0.], [1., -4., 1.], [0., 1., 0.]]]]).to(DEVICE)
    with torch.no_grad():
        z = torch.randn(num_samples, Z_DIM).to(DEVICE)
        fake_imgs = generator(z)
        if fake_imgs.shape[1] == 3:
            fake_imgs = 0.299 * fake_imgs[:, 0:1] + 0.587 * fake_imgs[:, 1:2] + 0.114 * fake_imgs[:, 2:3]
        fake_imgs = ((fake_imgs + 1) / 2 * 255)
        edges = F.conv2d(fake_imgs, laplacian_kernel, padding=1)
        variances = torch.var(edges.view(num_samples, -1), dim=1)
        high_quality_count = torch.sum(variances > variance_threshold).item()

    percentage = (high_quality_count / num_samples) * 100
    print(f"High-Resolution (Sharpness) Samples: {percentage:.1f}%")

