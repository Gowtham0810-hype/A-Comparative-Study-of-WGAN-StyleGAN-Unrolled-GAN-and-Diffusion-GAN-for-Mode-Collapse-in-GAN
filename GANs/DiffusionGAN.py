BATCH_SIZE = 64
IMG_SIZE = 64
Z_DIM = 128
DEVICE = torch.device("cuda" if torch.cuda.is_available() else "cpu")

def get_dataloaders(batch_size=BATCH_SIZE, img_size=IMG_SIZE):
    loaders = {}

    # 1. 2D Ring
    X_ring, _ = make_circles(n_samples=10000, noise=0.05, factor=0.5)
    loaders['2d_ring'] = DataLoader(TensorDataset(torch.tensor(X_ring, dtype=torch.float32)), batch_size=batch_size, shuffle=True)

    # 2. 2D Grid
    grid_points = []
    for i in range(-2, 3):
        for j in range(-2, 3):
            points = np.random.randn(400, 2) * 0.05 + np.array([i*2, j*2])
            grid_points.append(points)
    loaders['2d_grid'] = DataLoader(TensorDataset(torch.tensor(np.vstack(grid_points), dtype=torch.float32)), batch_size=batch_size, shuffle=True)

    # 3. MNIST
    transform_mnist = transforms.Compose([transforms.Resize(img_size), transforms.ToTensor(), transforms.Normalize([0.5], [0.5])])
    loaders['mnist'] = DataLoader(torchvision.datasets.MNIST('./data', train=True, download=True, transform=transform_mnist), batch_size=batch_size, shuffle=True, drop_last=True)

    # 4 & 5. MRI and CelebA
    transform_img = transforms.Compose([transforms.Resize((img_size, img_size)), transforms.ToTensor(), transforms.Normalize([0.5]*3, [0.5]*3)])
    try:
        loaders['mri'] = DataLoader(torchvision.datasets.ImageFolder('./data/mri', transform=transform_img), batch_size=batch_size, shuffle=True, drop_last=True)
        loaders['celeba'] = DataLoader(torchvision.datasets.ImageFolder('./data/celeba', transform=transform_img), batch_size=batch_size, shuffle=True, drop_last=True)
    except Exception as e:
        print(f"Note: Local paths for MRI/CelebA datasets need to be configured. {e}")

    return loaders

dataloaders = get_dataloaders()

class GeneratorCNN(nn.Module):
    def __init__(self, z_dim=Z_DIM, channels=3, img_size=IMG_SIZE):
        super().__init__()
        self.init_size = img_size // 16
        self.l1 = nn.Sequential(nn.Linear(z_dim, 512 * self.init_size ** 2))

        self.conv_blocks = nn.Sequential(
            nn.BatchNorm2d(512),
            nn.Upsample(scale_factor=2),
            nn.Conv2d(512, 256, 3, stride=1, padding=1),
            nn.BatchNorm2d(256), nn.ReLU(True),
            nn.Upsample(scale_factor=2),
            nn.Conv2d(256, 128, 3, stride=1, padding=1),
            nn.BatchNorm2d(128), nn.ReLU(True),
            nn.Upsample(scale_factor=2),
            nn.Conv2d(128, 64, 3, stride=1, padding=1),
            nn.BatchNorm2d(64), nn.ReLU(True),
            nn.Upsample(scale_factor=2),
            nn.Conv2d(64, channels, 3, stride=1, padding=1),
            nn.Tanh(),
        )

    def forward(self, z):
        out = self.l1(z)
        out = out.view(out.shape[0], 512, self.init_size, self.init_size)
        return self.conv_blocks(out)

class DiscriminatorCNN(nn.Module):
    def __init__(self, channels=3, img_size=IMG_SIZE):
        super().__init__()
        def disc_block(in_filters, out_filters, bn=True):
            block = [nn.Conv2d(in_filters, out_filters, 4, 2, 1), nn.LeakyReLU(0.2, inplace=True)]
            if bn: block.append(nn.BatchNorm2d(out_filters))
            return block

        self.model = nn.Sequential(
            *disc_block(channels, 64, bn=False),
            *disc_block(64, 128),
            *disc_block(128, 256),
            *disc_block(256, 512),
        )
        ds_size = img_size // 16
        self.adv_layer = nn.Linear(512 * ds_size ** 2, 1)

    def forward(self, img):
        out = self.model(img)
        out = out.view(out.shape[0], -1)
        return self.adv_layer(out)

